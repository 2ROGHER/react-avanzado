/**
 * Function para sumar dos valores cualquiera.
 * @param {number} a - Primer numero a sumar.
 * @param {number} b - Segundo numero a sumar. 
 * @returns {number} - Retorna un numero cualquiera.
 */

const suma = (a, b) => a + b;

/**
 * Function para agregar un item al final de un array.
 * @param {object []} array : Array inicial.
 * @param {object} item : El nuevo item que queremos agregar al array.
 * @returns {object[]} Devuelve un array de objetos.
 */
const pushArray = (array, item) => [...array, item];